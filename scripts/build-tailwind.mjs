import { compile, optimize } from '@tailwindcss/node';
import { Scanner } from '@tailwindcss/oxide';
import fs from 'fs/promises';
import path from 'path';
import process from 'process';

const projectRoot = process.cwd();
const sourceFile = path.join(projectRoot, 'src/index.css');
const outputFile = path.join(projectRoot, 'src/tailwind.generated.css');
const shouldMinify = process.env.NODE_ENV === 'production';

async function buildTailwind() {
  const sourceCss = await fs.readFile(sourceFile, 'utf8');
  const compiler = await compile(sourceCss, {
    from: sourceFile,
    base: path.dirname(sourceFile),
    shouldRewriteUrls: true,
    onDependency() {},
  });

  const sources = (
    compiler.root === 'none'
      ? []
      : compiler.root === null
        ? [{ base: projectRoot, pattern: '**/*', negated: false }]
        : [{ ...compiler.root, negated: false }]
  ).concat(compiler.sources);

  const scanner = new Scanner({ sources });
  const candidates = scanner.scan();
  let css = compiler.build(candidates);

  if (shouldMinify) {
    css = optimize(css, {
      minify: true,
      map: false,
    }).code;
  }

  const banner = [
    '/* This file is generated by scripts/build-tailwind.mjs */',
    `/* Candidates: ${candidates.length} */`,
    '',
  ].join('\n');

  await fs.writeFile(outputFile, `${banner}${css}\n`, 'utf8');

  return {
    outputFile,
    candidates: candidates.length,
    bytes: Buffer.byteLength(css, 'utf8'),
  };
}

try {
  const result = await buildTailwind();
  console.log(
    `tailwind:built ${path.relative(projectRoot, result.outputFile)} candidates=${result.candidates} bytes=${result.bytes}`,
  );
} catch (error) {
  console.error('tailwind:build-failed');
  console.error(error);
  process.exit(1);
}
